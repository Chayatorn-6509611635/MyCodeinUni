#Data Type & Variable
'''
Primitive Data Types
    Integers(int)
    Floating Point(float)
    Strings(str)
    Boolean(bool)
'''

#Declare Type (don't assign data type)
x = 10
name = "Anfield"
y = 20.23
z = True
print(x)
print(name)
print(y)
print(z)

#String concatenate (String + String)
print("x is " + str(x)) #casting type
print("name is " + name)
print("y is " + str(y))
print("Boolean is " + str(z))

#See Type
print(type(x))
print(type("Anfield"))