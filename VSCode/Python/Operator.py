# Operator (+, -, *, /)

a = 10
b = 3
print("a is", a)
print("a + b is", a + b)
print("a - b is", a - b)
print("a * b is", a * b) 
print("a / b is", a / b) 
print("a % b is", a % b)

print(type(a / b)) # int / int = float, but in C int / int = int

print("a pow b is", a ** b)
print("a // b is", a // b)
print("8 // 3 =", 8 // 3) #dont floor or ceiling remains