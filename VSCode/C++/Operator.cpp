#include<iostream>

using namespace std;

int main(){
    int a = 10, b = 3;
    cout << "a plus b equals " << a + b << endl;
    cout << "a minus b equals " << a - b << endl;
    cout << "a multiply by b equals " << a * b << endl;
    cout << "a divided by b equals " << a / b << endl; // int / int = int || a / b equals n 
    cout << "a modulo b equals " << a % b << endl; // a % b equals r
    // in Math a / b = n(result) + r(remainder)
    return 0;
}